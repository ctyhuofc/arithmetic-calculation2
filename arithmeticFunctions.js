function add(value1, value2){
    var answer= value1 + value2
    return answer
};

function sub(value1, value2){
    var answer= value1 - value2
    return answer
};

function multiply(value1, value2){
    var answer= value1 * value2
    return answer
};

function divide(value1, value2){
    var answer= value1 / value2
    return answer
};

module.exports = { add, sub,multiply,divide };


